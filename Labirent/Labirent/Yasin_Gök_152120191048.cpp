void labirent()
{
	int lab_boyutu;
	cout << "Labirent oyununa hosgeldiniz :)" << endl;
	cout << "Hareket etmek icin 'w' 'a' 's' 'd' ya da yon tuslarini kullaniniz !" << endl;
	cout << "+ = Altin, X = Canavar seklinde sembollenmistir." << endl << endl;
	cout << "Labirentin boyutunu giriniz: ";
	cin >> lab_boyutu;
	system("cls");

	if (lab_boyutu >= 10 && 100 >= lab_boyutu) {
		for (int i = 0; i < lab_boyutu; i++)										// Duvar ve bitis atama
		{
			for (int j = 0; j < lab_boyutu; j++)
			{
				manuel_labirent[0][j] = 1, manuel_labirent[lab_boyutu - 1][j] = 1;
			}
			manuel_labirent[i][0] = 1, manuel_labirent[i][lab_boyutu - 1] = 1;
		}
		manuel_labirent[lab_boyutu - 1][lab_boyutu - 2] = 2;

		for (int i = 0; i < lab_boyutu; i++)
		{
			for (int j = 0; j < lab_boyutu; j++)
			{
				switch (manuel_labirent[i][j])
				{
				case 1:
					cout << char(219);						// Duvar
					break;
				case 0:
					cout << " ";							// Yol
					break;
				case 2:
					cout << " ";							// Çýkýþ
					break;
				case 3:
					cout << char(88);						// Canavar  -->  X
					break;
				case 4:
					cout << char(43);						// Altýn	-->  +
					break;
				}
			}
			cout << " " << endl;
		}	
	}
	else {
		cout << "Boyutu 10-100 araliginda girmelisiniz !" << endl << endl;
		cout << "Programi sonlandirmak icin bir tusa basiniz !" << endl;
		_getch();
		system("pause");
	}
}

